
public class Operatory {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		System.out.println(a + b);
		System.out.println(6 + 3);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println(10 % 5);
		int c = 1;
		System.out.println(c);
		c++;
		System.out.println(c);
		c--;
		System.out.println(c);
		--c;
			System.out.println(c);
	}

}
